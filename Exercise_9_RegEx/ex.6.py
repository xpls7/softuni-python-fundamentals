import re

data = input()
pattern = r"(^|(?<=\s))w{3}\.[A-Za-z0-9-]+(\.[a-z]+)+($|(?=\s))"

while data:

    for el in re.finditer(pattern, data):
        print(el.group())

    data = input()

#  (^|(?<=\s) - ЗАПОЧВА реда или търси спейс ОТПРЕД
#  ($|(?=\s)) - ЗАВЪРШВА реда или търси спейс ОТЗАТ
#  w{3} - повтаря w 3 пъти
#  \. - поставя точка
#  [A-Za-z0-9-] - букви малко и големи, числа, и тире
#  \.[a-z]+  - започва с точка, има малки МНОГО букви

